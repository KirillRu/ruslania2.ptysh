    The son of a grocer and grandson of a serf, Anton Chekhov helped support his family, while he studied medicine, by writing humorous sketches. <br><br>

    His reputation as a master of the short story was assured when in 1888 The Steppe, a story in his third collection, won the Pushkin Prize. The Island of Sakhalin (1893-94) was a report on his visit to a penal colony in 1890. <br><br>

    Thereafter he lived in Melikhovo, near Moscow, where he ran a free clinic for peasants, took part in famine and epidemic relief, and was a volunteer census-taker. His first play, Ivanov (1887), had little success, but The Seagull (1898), Uncle Vanya (1899), The Three Sisters (1901), and The Cherry Orchard (1904) were acclaimed when produced by the Moscow Art Theater. <br><br>

    In 1901 Chekhov married the actress Olga Knipper, the interpreter of many of his characters. Three years later he died of tuberculosis. <br><br>

    The style of his stories, novels, and plays, emphasizing internal drama, characterization, and mood rather than plot and focusing on the tragicomic aspects of banal events, had great influence.<BR>

