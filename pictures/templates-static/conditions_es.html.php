  CONDICIONES PARA LAS COMPRAS A TRAVÉS DE INTERNET
  </div>
<br><br>

    Ruslania Books Oy (en adelante Ruslania) y sus clientes acuerdan 
    la aplicación de las siguientes condiciones para las compras 
    realizadas en su tienda a través de Internet.  Ruslania se 
    reserva el derecho de modificar las condiciones de entrega sin 
    previo aviso.  Antes de realizar una compra.  el cliente deberá 
    cerciorarse de que ha leído las condiciones vigentes de cada caso 
    en particular.  Los términos y condiciones de entrega de las 
    suscripciones se encuentran en la sección 
    <a href="http://www.ruslania.com/context-4168/entity-1/language-7.html">Condiciones de suscripción</a>.

  <br><br>
  <b>1. Condiciones generales de compra</b>
  <br><br>

    El cliente acepta junto a cada compra las condiciones de este 
    contrato vigentes para cada caso en particular.  que rigen tanto 
    para Finlandia como para el extranjero.  Ruslania sólo vende productos 
    a personas mayores de edad y a entidades constituidas.  La venta a 
    personas menores de edad sólo es posible bajo la condición de que 
    su tutor envíe un permiso por escrito.  por correo electrónico o 
    por fax.  autorizando la compra.

  <br><br>
  <b>2. Registro</b>
  <br><br>
    <b>2.1</b> 

    En su tienda online.  Ruslania vende productos sólo a usuarios 
    registrados.  Los clientes particulares deberán por tanto 
    indicar su nombre.  dirección.  número de teléfono y dirección 
    de correo electrónico.  Los clientes corporativos deberán indicar 
    también su código de identificación fiscal.  Ruslania se compromete 
    a no proveer información del cliente a terceros sin previa 
    autorización.  excepto a miembros del mismo grupo empresarial 
    o bajo requerimiento judicial.  En virtud de lo establecido en 
    la Ley de protección de datos personales.  cualquier cliente 
    tiene derecho a controlar su información personal al registrase.  
    Sólo el nombre.  dirección.  teléfono.  dirección de correo 
    electrónico.  número de usuario y contraseña permanecerán 
    registrados en el registro de clientes.  En el caso de que 
    el cliente lo desee se registrará también su lengua materna 
    y eventuales áreas de interés.  El registro del cliente permanecerá 
    bajo responsabilidad del servicio de atención al cliente.

  <br><br>
  <b>3. Precios</b>
    <br><br>
    
    <b>3.1</b> 

    El precio vigente es el indicado en la lista de precios de 
    Ruslania al momento de realizarse la orden de compra.  
    La lista de precios se encuentra en www.ruslania.com.  
    Todos los precios incluyen el IVA (10% ó 24%).

    <br><br>

    <b>3.2</b>

    Los cargos de envío serán añadidos a todos los envíos postales 
    según la forma de envío seleccionada por el cliente.  El servicio 
    de atención al cliente de Ruslania puede enviar los productos en 
    una o varias partidas.  siendo el costo de envío cobrado una sola vez.

    <br><br>

    <b>3.3</b> 
    La compra mínima en la tienda online es de 5 euros.  
    Ruslania se reserva el derecho de cambiar dicha suma.
    
  <br><br>
  <b>4. Pago de la orden de compra</b>
  <br><br>

    Vea en Cómo hacer una orden de compra la sección "Forma de pago"

  <br><br>
  <b>5. Condiciones de Envío</b>
  <br><br>
    <b>5.1</b> 

    Debido a que los libros y otros productos vendidos por 
    Ruslania son generalmente material no producido en Finlandia.  
    el plazo de entrega podrá variar entre 2 y 20 días laborables.  
    dependiendo de si el libro se encuentra o no en almacén.  
    En caso de no encontrarse éste en almacén.  el plazo estará sujeto 
    a la gestión y obtención del producto.

    <br><br>

    <b>5.2</b> 

    Si el plazo de entrega fuera más largo que el habitual.  
    Ruslania informará a la brevedad al cliente por correo electrónico.  
    En este caso.  el cliente tendrá derecho a cancelar su orden de 
    compra.  Ruslania se reserva el derecho a cancelar órdenes de 
    compra si el producto se encuentra agotado o fuera de stock o 
    si es excepcionalmente difícil de obtener.  Si esto sucediese. 
     se le ofrecerá al cliente un producto similar en su reemplazo.  
    o el cliente podrá conservar la suma abonada para compras futuras.

  <br><br>
  <b>6. Limitación de responsabilidad</b>
  <br><br>

    Ruslania no asume ninguna responsabilidad por daños o perjuicios 
    directos o indirectos que se deriven o se relacionen con el uso 
    que el cliente haga del producto.  así como tampoco en el caso 
    de que el pedido no sea aceptado u ocurriesen retrasos en el envío.  
    Ruslania tampoco asumirá ninguna responsabilidad respecto del 
    contenido del material.  así como tampoco de ningún daño que 
    derive de su uso.  siendo ésta responsabilidad del productor o 
    editor.  Similarmente.  todas las responsabilidades concernientes 
    a los derechos de autor (copyright) se en cuentran a cargo del 
    productor o del editor.

  <br><br>
  <b>7. Devoluciones</b>
  <br><br>

    <b>7.1</b> 

    Según la ley de Protección al consumidor.  
    cualquier producto enviado por correo a un cliente particular puede 
    ser devuelto dentro de un plazo de 14 días a partir de la fecha de 
    recepción.  Un producto devuelto deberá estar esencialmente en las 
    mismas condiciones en las que se encontraba al momento de ser recibido.
      El derecho de devolución no es aplicable a dvds.  vídeos.  cds o 
    casetes si las envolturas han sido abiertas.  ni tampoco es aplicable 
    al software transferido vía Internet.

    <br><br>

    <b>7.2</b> 

    Los casos de devolución por parte de clientes corporativos se 
    regirán por la legislación mercantil finlandesa y las normas de 
    la buena práctica comercial.  Cualquier devolución deberá ser 
    acordada con nuestro servicio de atención al cliente (ver "Contactos")

    <br><br>

    <b>7.3</b> 

    En el caso de devolverse un producto desde fuera de Finlandia.  
    los costes de envío serán asumidos por el cliente.  Cualquier 
    devolución deberá ser acordada con nuestro servicio de atención 
    al cliente (Ver <a href="http://www.ruslania.com/context-584/entity-1/language-7.html">Contactos</a>)

  <br><br>
  <b>8. Reclamaciones</b>
  <br><br>

    Si recibe un producto erróneo.  defectuoso o en mal estado.  
    el cliente deberá notificar inmediatamente a Ruslania (ver " Contactos").  
    El producto defectuoso se reemplazará.  por regla general.  
    enviando uno en buen estado en su reemplazo.  Si aquello no fuera 
    posible.  podrá obtener en su lugar.  por caso.  un descuento u otro 
    producto.  

  <br><br>
  <b>9. Fuerza Mayor</b>
  <br><br>

    Ruslania no asume responsabilidades en el caso de que el envío 
    del pedido se vea impedido.  retrasado.  o sufra complicaciones 
    en situaciones imprevistas que excedan su responsabilidad como parte.  
    Así como tampoco asumirá responsabilidades en caso de conflictos 
    laborales.  prohibiciones a la importación o exportación.  
    conflictos armados.  catástrofes naturales.  trastornos de 
    carácter general en medios de transporte o en la distribución 
    eléctrica.  o debido a otras causas similares en las que la 
    actuación de Ruslania o de sus proveedores se vea obstaculizada o 
    imposibilitada.  En caso de suceder alguna de estas situaciones.  
    Ruslania procurará ponerse en contacto con el cliente lo más pronto 
    posible.

  <br><br>
  <b>10. Solución de controversias</b>
  <br><br>

    A las controversias y/o desavenencias surgidas de estas condiciones 
    para las compras en Internet acordadas entre el cliente y Ruslania
     se aplicará la legislación finlandesa.  Los eventuales contenciosos 
    sobre la interpretación del convenio entre el cliente y Ruslania.  
    si no pudiesen resolverse mediante una negociación.  serán resueltos 
    en el Juzgado de Primera Instancia de la ciudad de Helsinki.

