<b>3.1</b> Le prix en vigueur est celui figurant au moment de la commande sur la liste des prix de Ruslania a l’adresse <a href="http://www.ruslania.com" class="maintxt">www.ruslania.com</a>. Tous les prix s’entendent TVA (10 % ou 24 %) incluse.
