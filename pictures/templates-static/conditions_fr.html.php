<b>5. Expedition </b>
<br><br>
    <b>5.1</b> S Ruslania vendant principalement des livres et d’autres produits d’importation, les delais de livraison vont de 2 a 20 jours ouvrables, selon si le produit est en stock ou non, et s’il ne l’est pas, selon le temps necessaire pour se le procurer.

