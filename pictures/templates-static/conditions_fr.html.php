    <b>3.2</b> Les frais d’expedition correspondant au mode de livraison choisi par le client seront ajoutes a toutes les commandes livrees par voie postale. Que les produits soient expedies par le service clients en un ou plusieurs envois, les frais d’expedition ne seront factures qu’une fois.
