    <b>5.2</b> Si les delais de livraison sont considerablement plus longs que d’ordinaire, Ruslania s’efforcera d’en informer le client dans les plus brefs delais par courrier electronique. Le client aura le droit d’annuler une commande portant sur un produit dont la livraison est ajournee de facon significative. Ruslania se reserve le droit d’annuler la commande si le produit commande est epuise ou si des difficultes insurmontables sont liees a son obtention. Dans cette eventualite, le client se verra proposer un autre produit a la place du produit epuise ou il pourra affecter la somme acquittee a des achats ulterieurs.


