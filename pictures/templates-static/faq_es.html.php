    <DIV><I><FONT color=#0000ff>
    ¿Por qué después de cliquear sobre "Actualizar mi cesto de compras" cambia la cantidad de la mercancía seleccionada?
    </FONT></I></DIV>
    <DIV>
    Seguramente se trata de que el programa, antes de calcular el precio de la mercancía, revisa la cantidad de la nominación disponible actualmente. Si por ejemplo, usted quiere comprar tres postales iguales y, después de actualizar vuestro cesto de compras, en lugar de tres postales la cantidad que aparece es "1", quiere decir que en estos momentos hay sólo una postal disponible en nuestra tienda. 
    </DIV>
    <DIV>&nbsp;</DIV>
    
    <DIV><I><FONT color=#0000ff>
    ¿Cual es la diferencia entre "Temporalmente fuera de stock" y "Producto no disponible"
    </FONT></I></DIV>
    <DIV>
    "Temporalmente fuera de stock" quiere decir que, actualmente, la mercancía se ha agotado en nuestro almacén pero muy pronto se reestablecerá. Sin embargo, "Producto no disponible" quiere decir que la mercancía no la tienen nuestros abastecedores o la edición se ha agotado completamente. En ese caso la nominación en cuestión no será recibida por un tiempo indeterminado. Usted podrá dejar una solicitud.
    </DIV>
    <DIV>&nbsp;</DIV>
    
    <DIV><I><FONT color=#0000ff>
    He hecho el pedido on-line. ¿Por qué no me lo han mandado inmediatamente?
    </FONT></I></DIV>
    <DIV>
    Además de la libreria on-line, Ruslania tiene una libreria tradicional en el centro de Helsinki. Desafortunadamente, pudiera ocurrir que un cliente en nuestra librería compre el último ejemplar y así tendriamos que esperar el próximo abastecimiento. Nosotros siempre informamos a nuestros clientes de cualquier retraso. 
    </DIV>
    <DIV>&nbsp;</DIV>
    
    <DIV><I><FONT color=#0000ff>
    ¿En qué caso es posible devolver la mercancia?
    </FONT></I></DIV>
    <DIV>
    La devolución de productos está regulada por la ley. Antes de devolver la mercancía, el cliente tiene que contactar el departamento de servicio al cliente (mire CONTACTOS). El derecho de devolución no se extiende a CDs, DVDs y software, si la empaquetadura original ha sido dañada por el cliente. El derecho de devolución tampoco se extiende a programas que han estado descargados por Internet. Si la mercancía es devuelta de otro país que no sea Finlandia – el cliente asume los gastos postales, a no ser que la mercancía se haya recibido defectuosa o resulte equivocada. 
    </DIV>
