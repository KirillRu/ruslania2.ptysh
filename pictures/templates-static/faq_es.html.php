    ¿Se pueden ver todas las rúbricas del sitio sin registrarse?</FONT></I></DIV>
    <DIV>
    Sí, se puede. Sólo que en ese caso usted no podrá hacer un pedido o dejar una solicitud. Además, los precios de los productos y de los gastos postales no corresponderían necesariamente con los de su región. Para los usuarios de la UE, los precios están indicados con el IVA (Impuesto al Valor Agregado) incluido, pero para los usuarios que residen en otros países – sin el impuesto.
