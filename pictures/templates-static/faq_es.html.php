    <DIV><I><FONT color=#0000ff>
    ¿Por qué, después de haber escrito mi dirección de correo electrónico y mi contraseña, yo no puedo entrar al sitio?
    </FONT></I></DIV>
    <DIV>
    Lo mas probable es que usted todavía no está registrado. En ese caso usted debiera registrarse y entonces se le hará posible entrar al sitio. Si usted ya está registrado, pero aparece la notificación "El nombre del usuario o la contraseña es incorrecta", seguramente usted ha indicado su dirección o contraseña con algún error.
    </DIV>
    <DIV>&nbsp;</DIV>
    
    <DIV><I><FONT color=#0000ff>
    ¿Es posible pagar con una tarjeta de crédito que no sea mía (de mi esposo, esposa, amigos, etc.)?
    </FONT></I></DIV>
    <DIV>
    Desafortunadamente no, ya que la transacción estaría hecha a nombre del propietario de la tarjeta de crédito. O sea, el usuario y el propietario tienen que ser la misma persona. Si usted quiere pagar con una tarjeta de crédito ajena, entonces el propietario de la tarjeta tendría que registrarse y hacer el pedido a su nombre. 
    </DIV>
    <DIV>&nbsp;</DIV>
    
    <DIV><I><FONT color=#0000ff>
    ¿Cuanto es fiable en Ruslania el procesamiento de las tarjetas de crédito?
    </FONT></I></DIV>
    <DIV>
    Ruslania no procesa las tarjetas de crédito y no tiene ningún acceso a ellas. Si el cliente paga su pedido con la tarjeta de crédito, después del trámite el pedido se redirecciona al sitio de la organización acreditada que se encarga de procesar tarjetas de crédito. En Finlandia esa compañía es LUOTTOKUNTA. Después de haber procesado la tarjeta de crédito el sistema nos transmite el informe de la transacción: aceptada (o no). Luego el usuario podrá regresar al sitio de Ruslania o cesar la sesión.
    </DIV>
    <DIV>&nbsp;</DIV>
    
    <DIV><I><FONT color=#0000ff>
    ¿Por qué el pago con la tarjeta de crédito no fue aceptado, aunque la tarjeta está vigente y tengo suficientes fondos en mi cuenta?
    </FONT></I></DIV>
    <DIV>
    Es posible que su tarjeta no tenga la opción de pago por Internet. Contacte el banco que le otorgó la tarjeta y averigüe si es así. Si todo está en orden, pero el pago aún no es aceptado, por favor, contáctenos. 
    </DIV>
    <DIV>&nbsp;</DIV>
    
    <DIV><I><FONT color=#0000ff>
