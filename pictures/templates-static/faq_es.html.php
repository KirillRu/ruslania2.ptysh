    <DIV><I><FONT color=#0000ff>
    ¿Qué es la "zona 5" y cómo se puede ver?
    </FONT></I></DIV>
    <DIV>
    Con el propósito de proteger de pirateo los DVD patentados,  existe actualmente un sistema de zonas por las que una película comprada en una determinada región del mundo sólo puede reproducirse en un reproductor de DVD que se haya fabricado para venderlo en esa zona. El mundo está dividido en 6 zonas. Rusia pertenece a la zona 5, los Estados Unidos y Canadá a la zona 1, Europa a la zona 2. Hay varias opciones para ver un DVD de la zona 5:
    <UL>
    <LI>Comprar un reproductor de DVD multizonal.</LI>
    <LI>Comprar en Rusia un reproductor de zona 5. </LI>
    <LI>Reproducir el video en un ordenador.</LI>
    <LI>Decodificar el lector de DVD cambiándole el código de zona que trae de fábrica.</LI>
    </DIV>
